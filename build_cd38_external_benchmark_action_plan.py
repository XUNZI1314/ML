from __future__ import annotations

import argparse
import csv
import json
from pathlib import Path
from typing import Any


ACTION_FIELDS = [
    "action_priority",
    "pdb_id",
    "method",
    "action_tier",
    "benchmark_gap",
    "package_expected",
    "current_status",
    "tool_available",
    "run_on",
    "pdb_input_status",
    "pdb_input_path",
    "expected_return_path",
    "required_pattern",
    "coverage_status",
    "recommended_batch_script",
    "recommended_command",
    "copy_back_instruction",
    "verification_command",
    "finalize_command",
    "next_action",
    "notes",
]


def _build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description=(
            "Build a single CD38 external benchmark action plan from expansion, preflight, "
            "readiness, and external-output import reports."
        )
    )
    parser.add_argument(
        "--package_dir",
        default="benchmarks/cd38/external_tool_inputs",
        help="Directory generated by prepare_cd38_external_tool_inputs.py.",
    )
    parser.add_argument(
        "--expansion_missing_actions_csv",
        default="benchmarks/cd38/expansion_plan/cd38_benchmark_missing_actions.csv",
        help="Missing PDB-method rows from build_cd38_benchmark_expansion_plan.py.",
    )
    parser.add_argument(
        "--readiness_summary_json",
        default="benchmarks/cd38/readiness/cd38_benchmark_readiness_summary.json",
        help="Readiness summary JSON from refresh_cd38_benchmark_readiness.py.",
    )
    parser.add_argument(
        "--out_dir",
        default="benchmarks/cd38/action_plan",
        help="Output directory for the consolidated action plan.",
    )
    parser.add_argument(
        "--benchmark_gaps_only",
        action="store_true",
        help="Only include actions that are still benchmark expansion gaps.",
    )
    return parser


def _repo_root() -> Path:
    return Path(__file__).resolve().parent


def _read_csv(path: Path) -> list[dict[str, str]]:
    if not path.exists():
        return []
    with path.open("r", encoding="utf-8", newline="") as handle:
        return [dict(row) for row in csv.DictReader(handle)]


def _write_csv(path: Path, rows: list[dict[str, Any]], fieldnames: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames, extrasaction="ignore")
        writer.writeheader()
        for row in rows:
            writer.writerow({field: row.get(field, "") for field in fieldnames})


def _load_json(path: Path) -> dict[str, Any] | None:
    if not path.exists():
        return None
    try:
        payload = json.loads(path.read_text(encoding="utf-8"))
    except Exception as exc:
        return {"load_error": str(exc), "path": str(path)}
    return payload if isinstance(payload, dict) else {"payload": payload}


def _write_json(path: Path, payload: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, ensure_ascii=True, indent=2), encoding="utf-8")


def _norm_pdb(value: Any) -> str:
    return str(value or "").strip().upper()


def _norm_method(value: Any) -> str:
    return str(value or "").strip().lower()


def _key(pdb_id: Any, method: Any) -> str:
    return f"{_norm_pdb(pdb_id)}:{_norm_method(method)}"


def _as_bool_text(value: bool) -> str:
    return "true" if value else "false"


def _safe_int(value: Any, default: int = 0) -> int:
    try:
        return int(float(str(value).strip()))
    except (TypeError, ValueError):
        return default


def _index_by_key(rows: list[dict[str, str]]) -> dict[str, dict[str, str]]:
    indexed: dict[str, dict[str, str]] = {}
    for row in rows:
        key = _key(row.get("pdb_id"), row.get("method"))
        if key != ":":
            indexed[key] = row
    return indexed


def _index_by_pdb(rows: list[dict[str, str]]) -> dict[str, dict[str, str]]:
    indexed: dict[str, dict[str, str]] = {}
    for row in rows:
        pdb_id = _norm_pdb(row.get("pdb_id"))
        if pdb_id:
            indexed[pdb_id] = row
    return indexed


def _method_status(status_row: dict[str, str] | None, method: str) -> str:
    if not status_row:
        return ""
    if method == "p2rank":
        return str(status_row.get("p2rank_status") or "")
    if method == "fpocket":
        return str(status_row.get("fpocket_status") or "")
    return ""


def _method_observed_path(status_row: dict[str, str] | None, method: str) -> str:
    if not status_row:
        return ""
    if method == "p2rank":
        return str(status_row.get("p2rank_predictions_csv") or "")
    if method == "fpocket":
        return str(status_row.get("fpocket_pockets_dir") or "")
    return ""


def _method_ready(status: str) -> bool:
    return str(status or "").startswith("ready")


def _tool_available(preflight_summary: dict[str, Any] | None, method: str) -> bool:
    tools = (preflight_summary or {}).get("tools") or {}
    if not isinstance(tools, dict):
        return False
    payload = tools.get(method) or {}
    return bool(payload.get("available")) if isinstance(payload, dict) else False


def _batch_script(method: str) -> str:
    if method == "p2rank":
        return "benchmarks/cd38/external_tool_inputs/run_p2rank_templates.ps1"
    if method == "fpocket":
        return "benchmarks/cd38/external_tool_inputs/run_fpocket_templates.ps1"
    return ""


def _individual_command(manifest_row: dict[str, str] | None, method: str) -> str:
    if not manifest_row:
        return ""
    if method == "p2rank":
        return str(manifest_row.get("recommended_p2rank_command") or "")
    if method == "fpocket":
        return str(manifest_row.get("recommended_fpocket_command") or "")
    return ""


def _copy_back_instruction(expected_return_path: str, package_dir: Path) -> str:
    if not expected_return_path:
        return f"Copy the completed output under {package_dir}."
    return f"Copy this path back under {package_dir}: {expected_return_path}"


def _next_action(
    *,
    method: str,
    benchmark_gap: bool,
    status: str,
    tool_available: bool,
    pdb_input_status: str,
    expected_return_path: str,
) -> str:
    if _method_ready(status):
        return "Output is already detected; run finalize_cd38_external_benchmark.py --run_discovered --run_sensitivity."
    if pdb_input_status and pdb_input_status != "ready":
        return "Regenerate external inputs with prepare_cd38_external_tool_inputs.py before running the external tool."
    target = "benchmark gap" if benchmark_gap else "external-package reproducibility row"
    if tool_available:
        return (
            f"Run the {method} template locally to fill this {target}, then rerun finalize. "
            f"Expected output: {expected_return_path}"
        )
    return (
        f"Run the {method} template on a machine/WSL environment with the tool installed, copy outputs back, "
        f"then rerun finalize. Expected output: {expected_return_path}"
    )


def _collect_action_rows(
    *,
    package_dir: Path,
    expansion_rows: list[dict[str, str]],
    expected_rows: list[dict[str, str]],
    preflight_rows: list[dict[str, str]],
    manifest_rows: list[dict[str, str]],
    coverage_rows: list[dict[str, str]],
    repair_rows: list[dict[str, str]],
    preflight_summary: dict[str, Any] | None,
    benchmark_gaps_only: bool,
) -> list[dict[str, Any]]:
    expansion_by_key = _index_by_key(expansion_rows)
    expected_by_key = _index_by_key(expected_rows)
    coverage_by_key = _index_by_key(coverage_rows)
    repair_by_key = _index_by_key(repair_rows)
    preflight_by_pdb = _index_by_pdb(preflight_rows)
    manifest_by_pdb = _index_by_pdb(manifest_rows)

    candidate_keys = set(expected_by_key) | set(expansion_by_key)
    rows: list[dict[str, Any]] = []
    for key in sorted(candidate_keys):
        pdb_id, method = key.split(":", 1)
        if method not in {"p2rank", "fpocket"}:
            continue
        benchmark_gap = key in expansion_by_key
        if benchmark_gaps_only and not benchmark_gap:
            continue
        package_expected = key in expected_by_key
        expansion_row = expansion_by_key.get(key) or {}
        expected_row = expected_by_key.get(key) or {}
        coverage_row = coverage_by_key.get(key) or {}
        repair_row = repair_by_key.get(key) or {}
        status_row = preflight_by_pdb.get(pdb_id) or {}
        manifest_row = manifest_by_pdb.get(pdb_id) or {}
        status = _method_status(status_row, method)
        observed_path = _method_observed_path(status_row, method)
        if _method_ready(status) and observed_path:
            expected_return_path = observed_path
        else:
            expected_return_path = str(expected_row.get("expected_return_path") or repair_row.get("expected_return_path") or "")
        tool_is_available = _tool_available(preflight_summary, method)
        pdb_input_status = str(status_row.get("pdb_input_status") or "")
        expansion_priority = _safe_int(expansion_row.get("priority"), default=0)
        manifest_priority = _safe_int(manifest_row.get("priority"), default=3)
        action_priority = expansion_priority if benchmark_gap else max(3, manifest_priority + 2)
        action_tier = "benchmark_completion" if benchmark_gap else "package_reproducibility"
        coverage_status = str(coverage_row.get("status") or coverage_row.get("coverage_status") or "")
        recommended_batch_script = _batch_script(method)
        recommended_command = _individual_command(manifest_row, method) or str(expansion_row.get("recommended_command") or "")
        next_action = _next_action(
            method=method,
            benchmark_gap=benchmark_gap,
            status=status,
            tool_available=tool_is_available,
            pdb_input_status=pdb_input_status,
            expected_return_path=expected_return_path,
        )
        rows.append(
            {
                "action_priority": action_priority,
                "pdb_id": pdb_id,
                "method": method,
                "action_tier": action_tier,
                "benchmark_gap": _as_bool_text(benchmark_gap),
                "package_expected": _as_bool_text(package_expected),
                "current_status": status or str(expansion_row.get("status") or coverage_status or "unknown"),
                "tool_available": _as_bool_text(tool_is_available),
                "run_on": "local" if tool_is_available else "external_machine_or_wsl",
                "pdb_input_status": pdb_input_status,
                "pdb_input_path": str(status_row.get("pdb_input_path") or manifest_row.get("tool_input_pdb_path") or ""),
                "expected_return_path": expected_return_path,
                "required_pattern": str(expected_row.get("required_pattern") or repair_row.get("required_pattern") or ""),
                "coverage_status": coverage_status,
                "recommended_batch_script": recommended_batch_script,
                "recommended_command": recommended_command,
                "copy_back_instruction": str(
                    repair_row.get("copy_back_instruction") or _copy_back_instruction(expected_return_path, package_dir)
                ),
                "verification_command": "python check_cd38_external_tool_environment.py",
                "finalize_command": "python finalize_cd38_external_benchmark.py --run_discovered --run_sensitivity",
                "next_action": next_action,
                "notes": str(expansion_row.get("notes") or expected_row.get("notes") or manifest_row.get("notes") or ""),
            }
        )
    rows.sort(key=lambda row: (_safe_int(row.get("action_priority"), default=99), row["action_tier"], row["pdb_id"], row["method"]))
    return rows


def _summarize(rows: list[dict[str, Any]], preflight_summary: dict[str, Any] | None, readiness_summary: dict[str, Any] | None) -> dict[str, Any]:
    benchmark_gap_rows = [row for row in rows if row.get("benchmark_gap") == "true"]
    package_only_rows = [row for row in rows if row.get("benchmark_gap") != "true"]
    ready_rows = [row for row in rows if _method_ready(str(row.get("current_status") or ""))]
    missing_benchmark_gap_rows = [
        row for row in benchmark_gap_rows if not _method_ready(str(row.get("current_status") or ""))
    ]
    if missing_benchmark_gap_rows:
        overall_status = "blocked_external_outputs_missing"
    elif package_only_rows and len(ready_rows) < len(rows):
        overall_status = "benchmark_complete_external_reproducibility_pending"
    else:
        overall_status = "ready_to_finalize_or_already_complete"
    tools = (preflight_summary or {}).get("tools") or {}
    status_counts: dict[str, int] = {}
    for row in rows:
        status = str(row.get("current_status") or "unknown")
        status_counts[status] = status_counts.get(status, 0) + 1
    return {
        "overall_status": overall_status,
        "action_count": len(rows),
        "benchmark_gap_action_count": len(benchmark_gap_rows),
        "package_reproducibility_action_count": len(package_only_rows),
        "ready_action_count": len(ready_rows),
        "missing_benchmark_gap_action_count": len(missing_benchmark_gap_rows),
        "status_counts": status_counts,
        "p2rank_tool_available": bool((tools.get("p2rank") or {}).get("available")) if isinstance(tools, dict) else False,
        "fpocket_tool_available": bool((tools.get("fpocket") or {}).get("available")) if isinstance(tools, dict) else False,
        "p2rank_missing_output_count": (preflight_summary or {}).get("p2rank_missing_output_count", ""),
        "fpocket_missing_output_count": (preflight_summary or {}).get("fpocket_missing_output_count", ""),
        "readiness_missing_or_pending_count": ((readiness_summary or {}).get("expansion_plan") or {}).get(
            "missing_or_pending_count", ""
        )
        if isinstance(readiness_summary, dict)
        else "",
    }


def _md(value: Any) -> str:
    return str(value).replace("|", "\\|")


def _build_report(summary: dict[str, Any], rows: list[dict[str, Any]]) -> str:
    lines = [
        "# CD38 External Benchmark Action Plan",
        "",
        "This file turns scattered CD38 external-tool blockers into an executable checklist.",
        "",
        "## Summary",
        "",
        f"- Overall status: `{summary['overall_status']}`",
        f"- Total action rows: `{summary['action_count']}`",
        f"- Benchmark-completion rows: `{summary['benchmark_gap_action_count']}`",
        f"- Package reproducibility rows: `{summary['package_reproducibility_action_count']}`",
        f"- Missing benchmark-completion rows: `{summary['missing_benchmark_gap_action_count']}`",
        f"- P2Rank on PATH: `{summary['p2rank_tool_available']}`",
        f"- fpocket on PATH: `{summary['fpocket_tool_available']}`",
        f"- Preflight missing P2Rank outputs: `{summary['p2rank_missing_output_count']}`",
        f"- Preflight missing fpocket outputs: `{summary['fpocket_missing_output_count']}`",
        "",
        "## Recommended Order",
        "",
        "1. Finish priority `1` fpocket rows for `3ROP` and `4OGW`; these close the highest-value ligand-bound benchmark gaps.",
        "2. Finish priority `2` rows for `3F6Y`; this adds an apo-like CD38 stress test for P2Rank/fpocket.",
        "3. If full external-package reproducibility is needed, also finish the package-only P2Rank rows for `3ROP` and `4OGW`.",
        "4. Copy returned files under `benchmarks/cd38/external_tool_inputs/`, then run `python finalize_cd38_external_benchmark.py --run_discovered --run_sensitivity`.",
        "",
        "## Action Rows",
        "",
        "| Priority | PDB | Method | Tier | Status | Run On | Expected Return |",
        "| ---: | --- | --- | --- | --- | --- | --- |",
    ]
    for row in rows:
        lines.append(
            "| {priority} | {pdb} | {method} | {tier} | {status} | {run_on} | `{expected}` |".format(
                priority=_md(row.get("action_priority", "")),
                pdb=_md(row.get("pdb_id", "")),
                method=_md(row.get("method", "")),
                tier=_md(row.get("action_tier", "")),
                status=_md(row.get("current_status", "")),
                run_on=_md(row.get("run_on", "")),
                expected=_md(row.get("expected_return_path", "")),
            )
        )
    lines.extend(
        [
            "",
            "## Commands",
            "",
            "- Prepare or refresh input package: `python prepare_cd38_external_tool_inputs.py`",
            "- Check local tools and missing outputs: `python check_cd38_external_tool_environment.py`",
            "- Run P2Rank template where P2Rank is installed: `benchmarks\\cd38\\external_tool_inputs\\run_p2rank_templates.ps1`",
            "- Run fpocket template where fpocket is installed: `benchmarks\\cd38\\external_tool_inputs\\run_fpocket_templates.ps1`",
            "- Finalize after outputs return: `python finalize_cd38_external_benchmark.py --run_discovered --run_sensitivity`",
            "",
            "## Detail Outputs",
            "",
            f"- CSV: `{summary['outputs']['action_plan_csv']}`",
            f"- JSON: `{summary['outputs']['action_plan_json']}`",
            f"- Markdown: `{summary['outputs']['action_plan_md']}`",
            "",
        ]
    )
    return "\n".join(lines)


def main() -> None:
    args = _build_parser().parse_args()
    repo_root = _repo_root()
    package_dir = (repo_root / args.package_dir).resolve() if not Path(args.package_dir).is_absolute() else Path(args.package_dir).resolve()
    out_dir = (repo_root / args.out_dir).resolve() if not Path(args.out_dir).is_absolute() else Path(args.out_dir).resolve()
    preflight_dir = package_dir / "preflight"
    imported_dir = package_dir / "imported_outputs"

    expansion_rows = _read_csv((repo_root / args.expansion_missing_actions_csv).resolve())
    expected_rows = _read_csv(package_dir / "cd38_external_tool_expected_returns.csv")
    preflight_rows = _read_csv(preflight_dir / "cd38_external_tool_preflight_status.csv")
    manifest_rows = _read_csv(package_dir / "cd38_external_tool_input_manifest.csv")
    coverage_rows = _read_csv(imported_dir / "cd38_external_tool_output_import_coverage.csv")
    repair_rows = _read_csv(imported_dir / "cd38_external_tool_output_import_repair_plan.csv")
    preflight_summary = _load_json(preflight_dir / "cd38_external_tool_preflight_summary.json")
    readiness_summary = _load_json((repo_root / args.readiness_summary_json).resolve())

    rows = _collect_action_rows(
        package_dir=package_dir,
        expansion_rows=expansion_rows,
        expected_rows=expected_rows,
        preflight_rows=preflight_rows,
        manifest_rows=manifest_rows,
        coverage_rows=coverage_rows,
        repair_rows=repair_rows,
        preflight_summary=preflight_summary,
        benchmark_gaps_only=bool(args.benchmark_gaps_only),
    )

    action_plan_csv = out_dir / "cd38_external_benchmark_action_plan.csv"
    action_plan_json = out_dir / "cd38_external_benchmark_action_plan.json"
    action_plan_md = out_dir / "cd38_external_benchmark_action_plan.md"
    summary = _summarize(rows, preflight_summary, readiness_summary)
    summary.update(
        {
            "package_dir": str(package_dir),
            "benchmark_gaps_only": bool(args.benchmark_gaps_only),
            "inputs": {
                "expansion_missing_actions_csv": str((repo_root / args.expansion_missing_actions_csv).resolve()),
                "expected_returns_csv": str(package_dir / "cd38_external_tool_expected_returns.csv"),
                "preflight_status_csv": str(preflight_dir / "cd38_external_tool_preflight_status.csv"),
                "preflight_summary_json": str(preflight_dir / "cd38_external_tool_preflight_summary.json"),
                "readiness_summary_json": str((repo_root / args.readiness_summary_json).resolve()),
                "import_coverage_csv": str(imported_dir / "cd38_external_tool_output_import_coverage.csv"),
                "import_repair_plan_csv": str(imported_dir / "cd38_external_tool_output_import_repair_plan.csv"),
            },
            "outputs": {
                "action_plan_csv": str(action_plan_csv),
                "action_plan_json": str(action_plan_json),
                "action_plan_md": str(action_plan_md),
            },
        }
    )
    payload = {"summary": summary, "actions": rows}

    _write_csv(action_plan_csv, rows, ACTION_FIELDS)
    _write_json(action_plan_json, payload)
    action_plan_md.parent.mkdir(parents=True, exist_ok=True)
    action_plan_md.write_text(_build_report(summary, rows), encoding="utf-8")

    print(f"Saved: {action_plan_csv}")
    print(f"Saved: {action_plan_json}")
    print(f"Saved: {action_plan_md}")
    print(f"Overall status: {summary['overall_status']}")
    print(f"Action rows: {summary['action_count']}")
    print(f"Benchmark gaps: {summary['benchmark_gap_action_count']}")


if __name__ == "__main__":
    main()
