from __future__ import annotations

import argparse
import json
import subprocess
import sys
from pathlib import Path
from typing import Any


def _build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Finalize the CD38 external P2Rank/fpocket benchmark workflow after outputs are copied back."
    )
    parser.add_argument(
        "--package_dir",
        default="benchmarks/cd38/external_tool_inputs",
        help="Directory generated by prepare_cd38_external_tool_inputs.py.",
    )
    parser.add_argument("--out_dir", default=None, help="Output directory. Defaults to <package_dir>/finalize.")
    parser.add_argument("--chain_filter", default="A", help="Chain filter used when importing external pocket outputs.")
    parser.add_argument("--rank_by_pdb", default="3ROP=2,4OGW=1", help="P2Rank rank overrides, e.g. 3ROP=2,4OGW=1.")
    parser.add_argument("--near_threshold", type=float, default=4.5, help="Near-hit threshold in Angstrom.")
    parser.add_argument(
        "--run_discovered",
        action="store_true",
        help="If readiness discovery finds runnable rows, import them into the CD38 benchmark panel.",
    )
    parser.add_argument(
        "--run_sensitivity",
        action="store_true",
        help="Run CD38 parameter sensitivity analysis after import. Useful after --run_discovered.",
    )
    parser.add_argument(
        "--import_source",
        default=None,
        help="Optional returned zip or directory to import before preflight/readiness, using import_cd38_external_tool_outputs.py.",
    )
    parser.add_argument("--import_overwrite", action="store_true", help="Overwrite existing files during --import_source.")
    parser.add_argument("--import_dry_run", action="store_true", help="Report import candidates without copying during --import_source.")
    parser.add_argument(
        "--skip_import_gate",
        action="store_true",
        help="Skip the pre-import return-package gate. Not recommended unless you have already reviewed the returned package.",
    )
    parser.add_argument(
        "--strict_import_gate",
        action="store_true",
        help="Exit non-zero when an enabled import gate does not return a PASS status. Useful for CI/automation.",
    )
    parser.add_argument("--python_executable", default=None, help="Python executable for child commands.")
    parser.add_argument("--continue_on_error", action="store_true", help="Continue child steps after a failure.")
    return parser


def _repo_root() -> Path:
    return Path(__file__).resolve().parent


def _run(command: list[str], cwd: Path) -> dict[str, Any]:
    completed = subprocess.run(command, cwd=str(cwd), check=False)
    return {
        "command": command,
        "returncode": int(completed.returncode),
        "status": "success" if int(completed.returncode) == 0 else "failed",
    }


def _load_json(path: Path) -> dict[str, Any] | None:
    if not path.exists():
        return None
    try:
        payload = json.loads(path.read_text(encoding="utf-8"))
    except Exception as exc:
        return {"load_error": str(exc), "path": str(path)}
    return payload if isinstance(payload, dict) else {"payload": payload}


def _write_json(path: Path, payload: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, ensure_ascii=True, indent=2), encoding="utf-8")


def _tool_ready(preflight: dict[str, Any] | None, tool: str) -> bool:
    if not preflight:
        return False
    key = f"{tool}_missing_output_count"
    return int(preflight.get(key) or 0) == 0


def _safe_int(value: Any) -> int:
    try:
        return int(value or 0)
    except (TypeError, ValueError):
        return 0


def _external_manifest_row_count(readiness: dict[str, Any] | None) -> int:
    if not readiness:
        return 0
    total = 0
    for tool in ("p2rank", "fpocket"):
        payload = readiness.get(tool) or {}
        if isinstance(payload, dict):
            total += _safe_int(payload.get("manifest_row_count"))
    return total


def _gate_status(gate_summary: dict[str, Any] | None) -> str:
    if not isinstance(gate_summary, dict):
        return ""
    return str(gate_summary.get("gate_status") or "")


def _build_next_actions(summary: dict[str, Any]) -> list[str]:
    preflight = summary.get("preflight_summary") or {}
    readiness = summary.get("readiness_summary") or {}
    import_summary = summary.get("import_summary") or {}
    action_plan = summary.get("action_plan_summary") or {}
    manifest_rows = _safe_int(summary.get("external_manifest_row_count"))
    actions: list[str] = []
    if summary.get("import_blocked_by_gate"):
        gate_summary = summary.get("import_gate_summary") if isinstance(summary.get("import_gate_summary"), dict) else {}
        gate_status = str(gate_summary.get("gate_status") or summary.get("import_gate_status") or "UNKNOWN")
        gate_message = str(gate_summary.get("decision_message") or "").strip()
        actions.append(f"Import was blocked by return-package gate: {gate_status}. {gate_message}".strip())
        actions.append("Run a dry-run import and review the gate report before attempting finalize again.")
    if summary.get("import_source") and not int(import_summary.get("candidate_file_count") or 0):
        diagnosis = str(import_summary.get("source_diagnosis") or "no_candidate_outputs_found")
        diagnostic_message = str(import_summary.get("diagnostic_message") or "").strip()
        coverage = import_summary.get("coverage") or {}
        actions.append(f"Import source diagnosis: {diagnosis}. {diagnostic_message}".strip())
        if coverage.get("missing_expected_outputs"):
            actions.append(f"Missing expected external outputs in returned source: {', '.join(coverage['missing_expected_outputs'])}.")
        if import_summary.get("repair_plan_csv"):
            actions.append(f"Open the repair plan CSV for exact missing paths and run templates: {import_summary['repair_plan_csv']}.")
        actions.append("Open the import scan CSV to inspect ignored files and confirm whether p2rank_outputs/ or fpocket_runs/*/*_out/ are present.")
    elif int(import_summary.get("dry_run_count") or 0):
        actions.append("Import dry run found output files; rerun without --import_dry_run to copy them into the package.")

    p2rank_missing = int(preflight.get("p2rank_missing_output_count") or 0)
    fpocket_missing = int(preflight.get("fpocket_missing_output_count") or 0)
    if bool(summary.get("run_discovered")) and not manifest_rows:
        actions.append("--run_discovered was requested, but readiness found 0 runnable P2Rank/fpocket rows; no new external benchmark rows were summarized.")
    if bool(summary.get("benchmark_import_attempted")):
        actions.append("Review imported benchmark results in benchmarks/cd38/results/cd38_benchmark_panel.md.")
        if not bool(summary.get("run_sensitivity")):
            actions.append("Optionally rerun with --run_sensitivity to refresh CD38 pocket parameter sensitivity tables.")
    if bool(summary.get("run_sensitivity")):
        actions.append("Review benchmarks/cd38/proxy_calibration/cd38_proxy_calibration_report.md before changing default pocket-overwide penalty settings.")
    if p2rank_missing or fpocket_missing:
        actions.append(
            "External outputs are still missing; run or adapt run_p2rank_templates.ps1 / run_fpocket_templates.ps1 on a machine with the tools installed."
        )
        action_plan_md = (summary.get("outputs") or {}).get("action_plan_md", "")
        if action_plan_md:
            actions.append(f"Open the consolidated external benchmark action plan for exact PDB-method priorities: {action_plan_md}.")
        actions.append("After outputs are copied back, rerun this finalize script.")
    elif not bool(summary.get("run_discovered")):
        actions.append("External outputs appear ready; rerun with --run_discovered to import them into the CD38 benchmark panel.")
    elif not bool(summary.get("benchmark_import_attempted")) and not manifest_rows:
        actions.append("External outputs appear present but no runnable rows were discovered; inspect P2Rank/fpocket readiness reports before rerunning.")

    if action_plan.get("overall_status") == "blocked_external_outputs_missing" and action_plan.get("missing_benchmark_gap_action_count"):
        plan_action = (
            f"Action plan still has {action_plan.get('missing_benchmark_gap_action_count')} missing benchmark-completion rows."
        )
        if plan_action not in actions:
            actions.append(plan_action)

    readiness_actions = readiness.get("next_actions") if isinstance(readiness, dict) else None
    if readiness_actions:
        for action in readiness_actions:
            if action not in actions:
                actions.append(str(action))
    return actions


def _md(value: Any) -> str:
    return str(value).replace("|", "\\|")


