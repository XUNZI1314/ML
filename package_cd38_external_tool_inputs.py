EXCLUDED_DIRS_DEFAULT = {
    "preflight",
    "finalize",
    "p2rank_outputs",
}


def _build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Package the CD38 external-tool input directory into a transfer zip for another machine or WSL/Linux."
    )
    parser.add_argument(
        "--package_dir",
        default="benchmarks/cd38/external_tool_inputs",
        help="Directory generated by prepare_cd38_external_tool_inputs.py.",
    )
    parser.add_argument(
        "--out_dir",
        default="benchmarks/cd38/external_tool_transfer",
        help="Output directory for the transfer zip and reports.",
    )
    parser.add_argument("--out_zip", default=None, help="Optional explicit output zip path.")
    parser.add_argument(
        "--include_existing_outputs",
        action="store_true",
        help="Include existing p2rank_outputs and fpocket *_out directories. Default is to package inputs only.",
    )
    parser.add_argument(
        "--root_name",
        default=None,
        help="Top-level directory name inside the zip. Defaults to the package directory name.",
    )
    parser.add_argument(
        "--skip_runbook_refresh",
        action="store_true",
        help="Do not refresh cd38_external_tool_next_run.* before packaging.",
    )
    return parser


def _write_csv(path: Path, rows: list[dict[str, Any]], fieldnames: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(rows)


def _role_for_path(relative_path: Path) -> str:
    text = relative_path.as_posix()
    if len(relative_path.parts) == 1:
        return ROOT_FILES.get(relative_path.name, "root_file")
    if relative_path.parts[0] == "pdbs" and relative_path.suffix.lower() == ".pdb":
        return "p2rank_input_pdb"
    if relative_path.parts[0] == "fpocket_runs" and relative_path.suffix.lower() == ".pdb":
        return "fpocket_input_pdb"
    if "p2rank_outputs" in relative_path.parts:
        return "existing_p2rank_output"
    if any(part.endswith("_out") for part in relative_path.parts):
        return "existing_fpocket_output"
    return f"package_file:{text}"


def _include_file(relative_path: Path, *, include_existing_outputs: bool) -> bool:
    if any(part == "__pycache__" for part in relative_path.parts):
        return False
    if relative_path.suffix.lower() in {".zip", ".pyc"}:
        return False
    if len(relative_path.parts) == 1:
        return relative_path.name in ROOT_FILES
    if not include_existing_outputs:
        if relative_path.parts[0] in EXCLUDED_DIRS_DEFAULT:
            return False
        if any(part.endswith("_out") for part in relative_path.parts):
            return False
    if relative_path.parts[0] == "pdbs" and relative_path.suffix.lower() == ".pdb":
        return True
    if relative_path.parts[0] == "fpocket_runs" and relative_path.suffix.lower() == ".pdb":
        return True
    if include_existing_outputs and (
        relative_path.parts[0] == "p2rank_outputs" or any(part.endswith("_out") for part in relative_path.parts)
    ):
        return True
    return False


def _iter_package_files(package_dir: Path, *, include_existing_outputs: bool) -> list[Path]:
    files: list[Path] = []
    for path in sorted(package_dir.rglob("*")):
        if not path.is_file():
            continue
        relative_path = path.relative_to(package_dir)
        if _include_file(relative_path, include_existing_outputs=include_existing_outputs):
            files.append(path)
    return files


def _refresh_runbook(package_dir: Path) -> dict[str, Any]:
    try:
        from build_cd38_external_tool_runbook import build_cd38_external_tool_runbook

        payload = build_cd38_external_tool_runbook(package_dir=package_dir)
        summary = payload.get("summary") if isinstance(payload, dict) else {}
        if not isinstance(summary, dict):
            summary = {}
        outputs = summary.get("outputs") if isinstance(summary.get("outputs"), dict) else {}
        return {
            "status": "success",
            "selected_action_count": summary.get("selected_action_count", ""),
            "report_md": outputs.get("report_md", ""),
            "powershell_runner": outputs.get("powershell_runner", ""),
            "bash_runner": outputs.get("bash_runner", ""),
        }
    except Exception as exc:
        return {"status": "failed", "error": str(exc)}


