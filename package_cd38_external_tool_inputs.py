def _write_zip(
    *,
    package_dir: Path,
    out_zip: Path,
    root_name: str,
    include_existing_outputs: bool,
) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    files = _iter_package_files(package_dir, include_existing_outputs=include_existing_outputs)
    out_zip.parent.mkdir(parents=True, exist_ok=True)
    with zipfile.ZipFile(out_zip, "w", compression=zipfile.ZIP_DEFLATED) as zf:
        for path in files:
            relative_path = path.relative_to(package_dir)
            archive_path = Path(root_name) / relative_path
            zf.write(path, archive_path.as_posix())
            rows.append(
                {
                    "archive_path": archive_path.as_posix(),
                    "source_path": str(path),
                    "size_bytes": int(path.stat().st_size),
                    "role": _role_for_path(relative_path),
                }
            )
        readme_path = f"{root_name}/TRANSFER_README.md"
        readme_text = _transfer_readme(root_name)
        zf.writestr(readme_path, readme_text)
        rows.append(
            {
                "archive_path": readme_path,
                "source_path": "generated",
                "size_bytes": len(readme_text.encode("utf-8")),
                "role": "transfer_readme",
            }
        )
    return rows


