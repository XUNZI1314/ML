def main() -> None:
    args = _build_parser().parse_args()
    package_dir = Path(args.package_dir).expanduser().resolve()
    if not package_dir.exists():
        raise FileNotFoundError(f"Package directory not found: {package_dir}")
    out_dir = Path(args.out_dir).expanduser().resolve()
    root_name = str(args.root_name or package_dir.name).strip() or "external_tool_inputs"
    out_zip = (
        Path(args.out_zip).expanduser().resolve()
        if args.out_zip
        else out_dir / "cd38_external_tool_inputs_transfer.zip"
    )
    runbook_refresh = {"status": "skipped"}
    if not bool(args.skip_runbook_refresh):
        runbook_refresh = _refresh_runbook(package_dir)

    rows = _write_zip(
        package_dir=package_dir,
        out_zip=out_zip,
        root_name=root_name,
        include_existing_outputs=bool(args.include_existing_outputs),
    )
    summary = {
        "package_dir": str(package_dir),
        "out_zip": str(out_zip),
        "out_dir": str(out_dir),
        "root_name": root_name,
        "include_existing_outputs": bool(args.include_existing_outputs),
        "runbook_refresh": runbook_refresh,
        "file_count": int(len(rows)),
        "total_source_bytes": int(sum(int(row["size_bytes"]) for row in rows)),
        "manifest_csv": str(out_dir / "cd38_external_tool_inputs_transfer_manifest.csv"),
        "summary_json": str(out_dir / "cd38_external_tool_inputs_transfer_summary.json"),
        "report_md": str(out_dir / "cd38_external_tool_inputs_transfer_report.md"),
    }
    _write_csv(Path(summary["manifest_csv"]), rows, MANIFEST_COLUMNS)
    Path(summary["summary_json"]).write_text(json.dumps(summary, ensure_ascii=True, indent=2), encoding="utf-8")
    Path(summary["report_md"]).write_text(_build_report(summary), encoding="utf-8")

    print(f"Saved: {out_zip}")
    print(f"Saved: {summary['manifest_csv']}")
    print(f"Saved: {summary['summary_json']}")
    print(f"Saved: {summary['report_md']}")
    print(f"Files packaged: {summary['file_count']}")


if __name__ == "__main__":
    main()
